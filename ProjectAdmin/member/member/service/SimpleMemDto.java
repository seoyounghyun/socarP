package member.service;

public class SimpleMemDto {

	private String smem_id;
	private String smem_name;
	private String smem_tel;
	private String smem_pwd;
	private java.util.Date smem_date;
		
	
	//게터세터
	public String getSmem_id() {
		return smem_id;
	}
	public void setSmem_id(String smem_id) {
		this.smem_id = smem_id;
	}
	public String getSmem_name() {
		return smem_name;
	}
	public void setSmem_name(String smem_name) {
		this.smem_name = smem_name;
	}
	public String getSmem_tel() {
		return smem_tel;
	}
	public void setSmem_tel(String smem_tel) {
		this.smem_tel = smem_tel;
	}
	public String getSmem_pwd() {
		return smem_pwd;
	}
	public void setSmem_pwd(String smem_pwd) {
		this.smem_pwd = smem_pwd;
	}
	public java.util.Date getSmem_date() {
		return smem_date;
	}
	public void setSmem_date(java.util.Date smem_date) {
		this.smem_date = smem_date;
	}
	
	
	
	
}
